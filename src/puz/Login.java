    private void saveAccountActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveAccountActionPerformed
        try {
            String username = un.getText();
            String password = new String(pw.getPassword());

            if (username.equals("") | password.equals("")) {
                JOptionPane.showMessageDialog(rootPane, "Fill All The Fields", "", JOptionPane.ERROR_MESSAGE);
                un.setText(null);
                pw.setText(null);

            } else {
                ResultSet rs = (ResultSet) dbConnection.search("select * from user where username='" + username + "'");
                if (rs.next()) {
                    JOptionPane.showMessageDialog(rootPane, "Username Allready Saved. Try to login or use a new username", "User Exsists", JOptionPane.WARNING_MESSAGE);
                } else {
                    dbConnection.iud("insert into user(username,password) values ('" + username + "','" + password + "')");
                    JOptionPane.showMessageDialog(rootPane, "Welcome To Puzzel World", "Welcome", JOptionPane.INFORMATION_MESSAGE);
                    un.setText(null);
                    pw.setText(null);
                    NewJFrame mainp = new NewJFrame();
                    mainp.setLocationRelativeTo(null);
                    mainp.setVisible(true);
                    this.dispose();
                }
            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_saveAccountActionPerformed

