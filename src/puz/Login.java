    private void LoginActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_LoginActionPerformed
        try {
            String username = un1.getText();
            String passw = new String(pw1.getPassword());

            ResultSet rs = (ResultSet) dbConnection.search("select * from user where username='" + username + "'");
            if (rs.next()) {
                ResultSet rs1 = (ResultSet) dbConnection.search("select * from user where username='" + username + "' and password='" + passw + "'");
                if (rs1.next()) {
                    JOptionPane.showMessageDialog(rootPane, "Welcome " + username, "Welcome", JOptionPane.INFORMATION_MESSAGE);
                    NewJFrame mainp = new NewJFrame();
                    mainp.myUsername(username);
                    mainp.setLocationRelativeTo(null);
                    mainp.setVisible(true);
                    this.dispose();
                }else{
                JOptionPane.showMessageDialog(rootPane, "kindly Check Password", "Wrong Password", JOptionPane.ERROR_MESSAGE);
                }
            } else {

                JOptionPane.showMessageDialog(rootPane, "Create an Account to Play", "Wrong Exsists", JOptionPane.ERROR_MESSAGE);

            }
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_LoginActionPerformed

